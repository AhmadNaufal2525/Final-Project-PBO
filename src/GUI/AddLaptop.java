    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        try {
            String sql = "INSERT INTO laptop"
            + "( computer_id, processor_speed, ram, hard_disk, weight,quantity, price,image)"
            + "VALUES (?,?,?,?,?,?,?,?)";
            pst = c.prepareStatement(sql);
            pst.setString(1, computerIDLp.getText());
            pst.setString(2, processor.getText());
            pst.setString(3, ram.getText());
            pst.setString(4, hardDisk.getText());
            pst.setString(5, weight.getText());
            pst.setInt(6, Integer.valueOf(quantity.getText()));
            pst.setInt(7, Integer.valueOf(price.getText()));
            pst.setBytes(8, laptop_image);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Inserted Successfully");
            
            DefaultTableModel model = (DefaultTableModel)laptopTable.getModel();
            model.setRowCount(0);
            showLaptop();
            clearInput();
        } catch (SQLException | HeadlessException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_addBtnActionPerformed


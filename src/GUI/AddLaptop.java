    public ArrayList<Laptop> getListLaptop ()
    {
        ArrayList<Laptop> ListLaptop = new ArrayList<Laptop>();
        String query = "SELECT * FROM `laptop`";
        Statement st;
        ResultSet rs;;
        
        try{
            st = c.createStatement();
            rs = st.executeQuery(query);
            
            Laptop lap;
            
            while(rs.next())
            {
                lap = new Laptop(
                                 rs.getString("computer_id"),
                                 rs.getString("processor_speed"),
                                 rs.getString("ram"),
                                 rs.getString("hard_disk"),
                                 rs.getString("weight"),
                                 rs.getInt("quantity"),
                                 rs.getInt("price"),
                                 rs.getBytes("image")
                                );
                ListLaptop.add(lap);
            }
            
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
        return ListLaptop;
    }
    
    public void showLaptop()
    {
        ArrayList<Laptop> Laptop = getListLaptop();
        DefaultTableModel model = (DefaultTableModel)laptopTable.getModel();
        model.setColumnIdentifiers(new Object[]{"computer_id","processor_speed","ram","hard_disk","weight","quantity","price"});
        Object[] row = new Object[7];
        
        for(int i = 0; i < Laptop.size(); i++)
        {
            row[0] = Laptop.get(i).getComputerID();
            row[1] = Laptop.get(i).getProcessor();
            row[2] = Laptop.get(i).getRam();
            row[3] = Laptop.get(i).getHardDisk();
            row[4] = Laptop.get(i).getWeight();
            row[5] = Laptop.get(i).getQuantity();
            row[6] = Laptop.get(i).getPrice();
            
            model.addRow(row);
        }
    }

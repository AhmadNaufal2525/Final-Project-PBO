    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int i = laptopTable.getSelectedRow();
        if (i == -1) {
            return;
        }
        String id = computerIDLp.getText();
        int pernyataan = JOptionPane.showConfirmDialog(null, "Are You Sure Data Will Be Deleted?","Confirm", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (pernyataan== JOptionPane.OK_OPTION) {
            try {
                String sql = "DELETE FROM laptop WHERE computer_id = ?";
                PreparedStatement pst = c.prepareStatement(sql);
                pst.setString(1, id);
                pst.executeUpdate();
                pst.close();
                DefaultTableModel model = (DefaultTableModel)laptopTable.getModel();
                model.setRowCount(0);
                showLaptop();
                JOptionPane.showMessageDialog(null, "Data Deleted");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Failed Deleted");
            } finally {
                clearInput();
            }
        }
        if (pernyataan == JOptionPane.CANCEL_OPTION) {
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

